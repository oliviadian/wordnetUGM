    <h5><b>Tentang WordNet</b></br></h5>
    Setiap pendapat, temuan, dan kesimpulan atau rekomendasi yang dinyatakan dalam materi ini adalah milik pencipta WordNet dan tidak serta merta mencerminkan pandangan lembaga pendanaan apa pun atau Universitas Princeton.

    Saat menulis makalah atau memproduksi aplikasi perangkat lunak, alat, atau antarmuka berbasis WordNet, perlu mengutip sumbernya dengan benar. Angka kutipan sangat penting untuk pendanaan WordNet.</br></br>

    <h5><b>Apa itu WordNet?</b></br></h5>
    WordNet® adalah basis data leksikal bahasa Inggris yang besar. Kata benda, kata kerja, kata sifat dan kata keterangan dikelompokkan ke dalam set sinonim kognitif (synsets), masing-masing mengekspresikan konsep yang berbeda. Sinkronisasi saling terkait melalui hubungan konseptual-semantik dan leksikal. Jaringan yang dihasilkan dari kata-kata dan konsep yang terkait secara bermakna dapat dinavigasi dengan browser. WordNet juga tersedia secara bebas dan publik untuk diunduh. Struktur WordNet menjadikannya alat yang berguna untuk linguistik komputasi dan pemrosesan bahasa alami.</br></br>

    WordNet dasar menyerupai tesaurus, di mana ia mengelompokkan kata bersama berdasarkan artinya. Namun, ada beberapa perbedaan penting. Pertama, WordNet menautkan bukan hanya bentuk kata — rangkaian huruf — tetapi kata-kata tertentu. Akibatnya, kata-kata yang ditemukan berdekatan satu sama lain dalam jaringan semantis disatukan. Kedua, WordNet memberi label hubungan semantik di antara kata-kata, sedangkan pengelompokan kata dalam tesaurus tidak mengikuti pola eksplisit apa pun selain makna kesamaan.

